CREATE OR REPLACE PROCEDURE REINICIAR_CALCULO_INTER (
   ANYO IN   NUMBER,
   NUM_EXP IN NUMBER
)
IS

CONTADOR NUMBER(4);

BEGIN

	SELECT COUNT(1) INTO CONTADOR FROM AA79BA3T00 WHERE ANYO_0A3 = ANYO AND NUM_EXP_0A3 = NUM_EXP;

  	if (CONTADOR > 0) THEN
  		
		DELETE FROM AA79BA6T00 WHERE ID_0A6 IN (SELECT ID_058 FROM AA79B58T00 WHERE ANYO_058 = ANYO AND NUM_EXP_058 = NUM_EXP);

		DELETE FROM AA79BA3T00 WHERE ANYO_0A3 = ANYO AND NUM_EXP_0A3 = NUM_EXP;

	END IF; 
		
END;
