FUNCTION PARSE_HORA_MINUTOS ( HORAS IN VARCHAR) RETURN NUMBER IS

RETORNO NUMBER(9,2):= 0.0;
   
begin
	RETORNO:= TO_NUMBER(SUBSTR(HORAS,1, instr(HORAS, ':',1,1)-1)); 
	IF (TO_NUMBER(SUBSTR(HORAS, instr(HORAS, ':',1,1)+1))>0) THEN
		RETORNO:= RETORNO + ROUND((TO_NUMBER(SUBSTR(HORAS, instr(HORAS, ':',1,1)+1))*60)/3600,2);
    END IF;
	return RETORNO;
END;
/