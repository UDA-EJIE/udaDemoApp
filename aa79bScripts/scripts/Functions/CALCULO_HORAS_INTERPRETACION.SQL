CREATE OR REPLACE FUNCTION CALCULO_HORAS_INTERPRETACION( ANYO IN   NUMBER,
   CORR_EXP IN NUMBER) RETURN VARCHAR IS

RETORNO VARCHAR2(10):='';

FECHA_INI DATE;
FECHA_FIN DATE;

BEGIN
		
	SELECT FECHA_INI_052, FECHA_FIN_052 into FECHA_INI, FECHA_FIN FROM AA79B52T00 WHERE NUM_EXP_052 = CORR_EXP AND ANYO_052 = ANYO;
	
	RETORNO:= HORAS_ENTRE_FECHAS_CALENDAR(FECHA_INI,FECHA_FIN);
	
	RETURN RETORNO;
END;
