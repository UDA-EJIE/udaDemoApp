CREATE OR REPLACE VIEW X54JAPI_TRABAJADORES_EMPRESA AS 
SELECT TIPIDEN_006 AS TIPIDEN
, PREDNI_006 AS PREDNI
, DNI_006 AS DNI
, SUFDNI_006 AS SUFDNI
, NOMBRE_006 AS NOMBRE
, APEL1_006 AS APEL1
, APEL2_006 AS APEL2
, ESTADO_006 AS ESTADO
, ITIPENT_036 AS TIPO_ENTIDAD
, CODENT_036 AS COD_ENTIDAD
, NOMBRE_037 AS DESC_ES
, NOMBRE_037 AS DESC_EU
, CIF_037 AS CIF
, FACTURABLE_036 AS FACTURABLE
, IVA_036 AS IVA
, CDIRNORA_037 AS CDIRNORA
FROM X54J06T00
LEFT JOIN X54J38T00 ON DNI_006 = DNITRAB_038
LEFT JOIN X54J37T00 ON CEMPRESA_038 = CEMPRESA_037
LEFT JOIN X54J36T00 ON CEMPRESA_037 = CODENT_036 AND CAPLIC_036 = 7;
/
