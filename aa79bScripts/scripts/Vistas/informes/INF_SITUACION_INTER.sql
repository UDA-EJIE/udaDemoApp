CREATE OR REPLACE VIEW INF_SITUACION_INTER ( 
URTEA, ESPEDIENTEA, ESPEDIENTE_MOTA, ESKATZAILEA, 
ESKATZAILE_MOTA, ESKATZAILE_HARREMANETARAKO, ESKATZAILE_HARREMANETARAKO_NAN,
ATAZA, NORI_ESLEITUA_ZENBAKIA, NORI_ESLEITUA, ESKAERA_DATA, AMAIERA_DATA, 	
HASIERA_DATA, 
FAKTURATUTAKO_INTERPR_KOPURUA, FAKTURATUTAKO_ORDUAK,	
AURREKUSITAKO_DATA, EGIKARATZE_DATA, AMAITUTA, EKITALDI_MOTA,
DIRU_SARRERA, BEZ, ZENBATEKOA_BEZ_BARNE, ATAZAREN_KODEA, BAZTERTUTA, 
BAZTERTZEKO_ARRAZOIA, EZEZTATUTA, EZEZTATZEKO_ARRAZOIA )
AS 
SELECT ANYO_051, NUM_EXP_051, 'INTEPRETAZIOA', nvl(DESC_EU, DESC_ES), TIPO, 
TRIM(t71solici.APEL1_077) || ' ' || TRIM(t71solici.APEL2_077) || ', ' || TRIM(t71solici.NOMBRE_077) ,(t71solici.PREDNI_077 ||t71solici.DNI_077 || t71solici.SUFDNI_077),
DESC_EU_015,
DECODE(DNI_RECURSO_081, null, '-', (t71recurso.PREDNI_077 ||t71recurso.DNI_077 || t71recurso.SUFDNI_077)),
DECODE(DNI_RECURSO_081, null, DESC_LOTE_EU_029, TRIM(t71recurso.APEL1_077) || ' ' || TRIM(t71recurso.APEL2_077) || ', ' || TRIM(t71recurso.NOMBRE_077)), 
FECHA_ALTA_051, FECHA_FIN_052, FECHA_INI_052, NUM_INTERPRETES_0A3, HORAS_INTERPRETACION_0A3,
FECHA_FIN_081, EJECUCION_TAREA.FECHA_EJECUCION_082, DECODE(ESTADO_EJECUCION_081, 3, 'Bai', 'Ez'), nvl(DESC_EU_008, DESC_ES_008),
IMPORTE_BASE_0A3, POR_IVA_0A3, IMPORTE_TOTAL_0A3, ID_TAREA_081,
DECODE(ID_RECHAZO_059, null, 'Ez', 'Bai'),
DECODE(ID_RECHAZO_059, null, '-', (DESC_EU_013)),
DECODE(ID_ANULACION_059, null, 'Ez', 'Bai'),
DECODE(ID_ANULACION_059, null, '-', (DESC_EU_012))
FROM AA79B51T00 
LEFT JOIN AA79B81T00 ON ANYO_081 = ANYO_051 AND NUM_EXP_081 = NUM_EXP_051
LEFT JOIN AA79B82T00 EJECUCION_TAREA ON EJECUCION_TAREA.ID_TAREA_082 = ID_TAREA_081
JOIN AA79B54T00 ON ANYO_051 = ANYO_054 AND NUM_EXP_051 = NUM_EXP_054
JOIN X54JAPI_ENTIDADES_SOLIC ON TIPO =  TIPO_ENTIDAD_054 AND CODIGO = ID_ENTIDAD_054
JOIN AA79B77T00 t71solici ON t71solici.DNI_077 = DNI_SOLICITANTE_054
LEFT JOIN AA79B15T00 ON ID_TIPO_TAREA_081 = ID_015
LEFT JOIN AA79B77T00 t71recurso ON t71recurso.DNI_077 = DNI_RECURSO_081
LEFT JOIN AA79B29T00 ON ID_LOTE_029 = ID_LOTE_081
JOIN AA79B52T00 ON ANYO_051 = ANYO_052 AND NUM_EXP_051 = NUM_EXP_052
LEFT JOIN AA79BA3T00 ON ANYO_051 = ANYO_0A3 AND NUM_EXP_051 = NUM_EXP_0A3
JOIN AA79B59T00 ON NUM_EXP_051 = NUM_EXP_059 AND ANYO_051 = ANYO_059 AND ESTADO_BITACORA_051 = ID_ESTADO_BITACORA_059
LEFT JOIN AA79B67T00 ON ID_RECHAZO_059 = ID_067
LEFT JOIN AA79B68T00 ON ID_067 = ID_068
LEFT JOIN AA79B13T00 ON ID_MOTIVO_RECHAZO_067 = ID_013
LEFT JOIN AA79BA1T00 ON ID_ANULACION_059 = ID_0A1
LEFT JOIN AA79BA2T00 ON ID_0A1 = ID_0A2
LEFT JOIN AA79B12T00 ON ID_MOTIVO_ANULACION_0A1 = ID_012
LEFT JOIN AA79B08S01 ON TIPO_ACTO_052 = ID_008
WHERE ID_TIPO_EXPEDIENTE_051 = 'I';
