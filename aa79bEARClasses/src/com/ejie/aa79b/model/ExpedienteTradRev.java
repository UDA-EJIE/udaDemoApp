	/**
	 * Devuelve el numPalIzo y los tramos de concordancia maquetados
	 *
	 * @return
	 */
	public String getNumTotalPalConTramos() {
		DecimalFormatSymbols symbols = new DecimalFormatSymbols();
		symbols.setDecimalSeparator(',');
		DecimalFormat formatter = new DecimalFormat("0.##", symbols);

		StringBuilder sbNumPal = new StringBuilder(Constants.SB_INIT);
		sbNumPal.append("<div class=\"numPalIzoConcor\"><div class=\"numPalIzoConcor1\">");
		if (this.getTradosExp() != null && this.getTradosExp().getNumTotalPal() != null
				&& this.getTradosExp().getNumTotalPal() > 0) {
			sbNumPal.append(formatter.format(this.getTradosExp().getNumTotalPal()));
		} else {
			sbNumPal.append(this.getNumTotalPalIzo() != null ? this.getNumTotalPalIzo() : "-");
		}
		sbNumPal.append("<br /></div>");
		if (this.getTradosExp() != null && comprobarRangosTrados()) {
			sbNumPal.append("<div class=\"numPalIzoConcor2\"><b>0-84%: </b>");
			sbNumPal.append(formatter.format(this.getTradosExp().getNumPalConcor084()));
			sbNumPal.append("<br /><b>85-94%: </b>");
			sbNumPal.append(formatter.format(this.getTradosExp().getNumPalConcor8594()));
			sbNumPal.append("<br /><b>95-100%: </b>");
			sbNumPal.append(formatter.format(this.getTradosExp().getNumPalConcor95100()));
			sbNumPal.append("<br /></div>");
		}
		sbNumPal.append("</div>");
		return sbNumPal.toString();
	}

